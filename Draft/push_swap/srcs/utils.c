#include "push_swap.h"

int	ft_strcmp(char *s1, char *s2)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	return (s1[i] - s2[i]);
}

void	is_sorted(t_stack *a)
{
	while (a->next)
	{
		if (a->num > a->next->num)
			return ;
		a = a->next;
	}
	free_exit(&a);
}

int	is_macro(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == ' ')
			return (1);
		i++;
	}
	return (0);
}
